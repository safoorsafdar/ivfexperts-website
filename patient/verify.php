        <link rel="stylesheet" href="https://cdnjs.cloudflare.com/ajax/libs/font-awesome/6.5.0/css/all.min.css">
        <style>
            body { font-family: 'Noto Sans', sans-serif; }
            h2, .font-heading { font-family: 'Figtree', sans-serif; }
            .medical-bg {
                background-color: #F8FAFC;
                background-image: radial-gradient(at 0% 0%, hsla(192, 91%, 91%, 1) 0, transparent 50%), 
                                  radial-gradient(at 50% 0%, hsla(152, 63%, 92%, 1) 0, transparent 50%);
            }
        </style>
    </head>
    <body class="medical-bg min-h-screen flex items-center justify-center p-6 text-slate-900">
        <div class="bg-white border border-slate-200 rounded-[2.5rem] p-10 max-w-sm w-full text-center shadow-xl shadow-slate-200/50">
            <div class="w-16 h-16 bg-rose-50 rounded-2xl flex items-center justify-center mx-auto mb-5">
                <i class="fa-solid fa-qrcode text-rose-500 text-2xl"></i>
            </div>
            <h2 class="text-xl font-bold text-slate-800 mb-2">QR Code Not Found</h2>
            <p class="text-slate-400 text-sm mb-6 font-medium">This QR code is invalid, expired, or was not generated by IVF Experts.</p>
            <a href="index.php" class="block w-full bg-cyan-600 text-white py-4 rounded-2xl font-bold text-sm hover:bg-cyan-700 shadow-lg shadow-cyan-100 transition-all">
                Go to Patient Portal
            </a>
        </div>
    </body>
    </html>
    <?php
    exit;
}

// ── If already authenticated as the correct patient — show document directly ──
if (isset($_SESSION['portal_patient_id']) && $_SESSION['portal_patient_id'] == $doc_patient_id) {
    // Redirect to view.php which handles the document rendering
    header("Location: view.php?type={$doc_view_type}&hash={$hash}");
    exit;
}

// ── CNIC-only mini-verification ────────────────────────────────────────────────
// Fetch patient name and masked CNIC for the hint
try {
    $stmt = $conn->prepare("SELECT first_name, last_name, cnic, phone FROM patients WHERE id = ?");
    $stmt->bind_param('i', $doc_patient_id);
    $stmt->execute();
    $doc_patient = $stmt->get_result()->fetch_assoc();
}
catch (Exception $e) {
    $doc_patient = null;
}

// Mask the CNIC for the hint — show first 5 digits only
$cnic_clean_db = preg_replace('/[^0-9]/', '', $doc_patient['cnic'] ?? '');
$cnic_hint = !empty($cnic_clean_db) ? substr($cnic_clean_db, 0, 5) . '-XXXXXXX-X' : '';

// Phone hint — last 4 digits
$phone_db = $doc_patient['phone'] ?? '';
$phone_hint = !empty($phone_db) ? 'XXXXXX' . substr($phone_db, -4) : '';

$error = '';

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    if (!csrf_verify()) {
        $error = "Security token mismatch. Please try again.";
    }
    elseif (!check_rate_limit('verify_attempts_' . $hash)) {
        $error = "Too many failed attempts for this document. Please wait a few minutes.";
    }
    else {
        $cnic_input = preg_replace('/[^0-9]/', '', $_POST['cnic'] ?? '');

        if (strlen($cnic_input) < 13) {
            $error = "Please enter your complete 13-digit CNIC number.";
        }
        elseif ($cnic_input !== $cnic_clean_db) {
            $error = "Verification failed. Please enter the 13-digit CNIC number exactly as on your identity card.";
        }
        else {
            // Verified! Set session and redirect to document
            session_regenerate_id(true);
            $_SESSION['portal_patient_id'] = $doc_patient_id;
            $_SESSION['session_start'] = time();
            $_SESSION['last_activity'] = time();
            header("Location: view.php?type={$doc_view_type}&hash={$hash}");
        }
    }
}
